    public void getValues(int aid) {
        this.aid = aid;
        String sql ="select projektledare.aid from projektledare join anstalld on projektledare.aid=anstalld.aid where anstalld.aid="+aid;
        String sql2 ="select specialist.aid from specialist join anstalld on specialist.aid=anstalld.aid where anstalld.aid="+aid;
        
        
        try {
            String leader = databas.fetchSingle(sql);
            String special = databas.fetchSingle(sql2);
            if(leader!=null){
            projradio.setSelected(true);
            this.orgleader=true;
            }
            
            if(special!=null){
            specRadio.setSelected(true);
            this.orgspecial=true;
            }
        }
        
        catch (InfException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
    public String getName(int aid) {
        String sql ="SELECT NAMN from anstalld where anstalld.aid="+aid;
        String name="";
        try {
        name = databas.fetchSingle(sql);
        }catch (InfException ex) {
            System.out.println(ex.getMessage());
        }
        return name;
    }
    
