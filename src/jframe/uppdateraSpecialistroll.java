    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String projsql = "";
        String specsql = "";
        
        if(orgleader != projradio.isSelected()) {
            try {
                if(projradio.isSelected()){
                    projsql = "insert into PROJEKTLEDARE (aid) values ("+aid+")";
                    databas.insert(projsql);
                    }
                else if(!projradio.isSelected()){
                    projsql ="delete from PROJEKTLEDARE WHERE AID="+aid;
                    databas.delete(projsql);
                }
                    JOptionPane.showMessageDialog(null, getName(aid)+"s projektledarroll har blivit uppdaterad!");
                } 
                catch (InfException e) {  
                    System.out.println(e.getMessage());
                    if(e.getErrorCode()==0) 
                        JOptionPane.showMessageDialog(null, "Den anställde är satt som projektledare för minst ett projekt, byt projektledare för projekt innan dess projektledarroll kan tas bort.");
                    else
                    JOptionPane.showMessageDialog(null, "Något gick fel vid kontakten med databasen");

                }
            }
        if(orgspecial != specRadio.isSelected()) {
            try {
                if(specRadio.isSelected()){
                    specsql = "insert into SPECIALIST (aid) values ("+aid+")";
                    databas.insert(specsql);}
                else if(!specRadio.isSelected()){
                    specsql ="delete from SPECIALIST WHERE AID="+aid;  
                    databas.delete(specsql);
                }
                    JOptionPane.showMessageDialog(null, getName(aid)+"s specialistroll har blivit uppdaterad!");
                } 
                catch (InfException e) {  
                    System.out.println(e.getMessage());
                    JOptionPane.showMessageDialog(null, "Något gick fel vid kontakten med databasen");

                }
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

